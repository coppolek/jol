for (let i = 1; i < lines.length; i++) {
  const line = lines[i].trim();
  if (!line) continue;
  const parts = line.split(';');
  if (parts.length >= 3) {
    const name = parts[0].replace(/"/g, '').trim();
    const address = parts[1].replace(/"/g, '').trim();
    const city = parts[2].replace(/"/g, '').trim();
    if (name) {
      newSites.push({
        id: `s200${i}`,
        name,
        address,
        city,
        schedules: [],
        assignedOperatorId: null
      });
    }
  }
}

